    private void populateStudentTable(DefaultTableModel stdTableModel){
        ArrayList<Student> stdList = Student.getAllStudents();
        if(stdTableModel.getRowCount()>0){
            for(int i=0; i<stdTableModel.getRowCount(); i++){
                stdTableModel.removeRow(i);
            }
        }
        for(int i=0; i<stdList.size(); i++){
            Object[] row = new Object[10];
            row[0] = stdList.get(i).getId();
            row[1] = stdList.get(i).getName();
            row[2] = stdList.get(i).getSportCenterCode();
            row[3] = stdList.get(i).getCoachCode();
            row[4] = stdList.get(i).getSportCode();
            row[5] = stdList.get(i).getAge();
            row[6] = stdList.get(i).getAddress();
            stdTableModel.addRow(row);
        }
    }
    private void populateSportTable(DefaultTableModel sportTableModel){
        ArrayList<Sport> sportList = Sport.getAllSports();
        if(sportTableModel.getRowCount()>0){
            for(int i=0; i<sportTableModel.getRowCount(); i++){
                sportTableModel.removeRow(i);
            }
        }
        for(int i=0; i<sportList.size(); i++){
            Object[] row = new Object[4];
            row[0] = sportList.get(i).getCode();
            row[1] = sportList.get(i).getName();
            row[2] = sportList.get(i).getFees();
            row[3] = sportList.get(i).getSchedule();
            sportTableModel.addRow(row);
        }
    }
    private void populateCoachTable(DefaultTableModel coachTableModel){
        ArrayList<Coach> coachList = Coach.getAllCoaches();
        if(coachTableModel.getRowCount()>0){
            for(int i=0; i<coachTableModel.getRowCount(); i++){
                coachTableModel.removeRow(i);
            }
        }
        for(int i=0; i<coachList.size(); i++){
            Object[] row = new Object[10];
            row[0] = coachList.get(i).getId();
            row[1] = coachList.get(i).getName();
            if(coachList.get(i).getDateJoined()!=null)row[2] = coachList.get(i).getDateJoined().getTime(); else row[2] = coachList.get(i).getDateJoined();
            if(coachList.get(i).getDateTerminated()!=null)row[3] = coachList.get(i).getDateTerminated().getTime(); else row[3] = coachList.get(i).getDateTerminated();
            row[4] = coachList.get(i).getHourlyRate();
            row[5] = coachList.get(i).getPhone();
            row[6] = coachList.get(i).getAddress();
            row[7] = coachList.get(i).getSportCenterCode();
            row[8] = coachList.get(i).getSportCode();
            row[9] = coachList.get(i).getRatings();
            coachTableModel.addRow(row);
        }
    }
